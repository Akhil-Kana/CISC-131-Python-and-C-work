import math

print("Zero:", int((4 / 4) - (4 / 4)))
print("One:", int((4 / 4) + (4 - 4)))
print("Two:", int((4 / 4) + (4 / 4)))
print("Three:", int(math.sqrt(4) + math.sqrt(4) - (4 / 4)))
print("Four:", int(math.sqrt(4) + math.sqrt(4) - (4 - 4)))
print("Five:", int(math.sqrt(4) + math.sqrt(4) + (4 / 4)))
print("Six:", int(4 + math.sqrt(4) - (4 - 4)))
print("Seven:", int(4 + math.sqrt(4) + (4 / 4)))
print("Eight:", int(4 + 4 - (4 - 4)))
print("Nine:", int(4 + 4 + (4 / 4)))
